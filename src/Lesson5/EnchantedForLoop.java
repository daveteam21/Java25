package Lesson5;

public class EnchantedForLoop
{

	public static void main(String[] args)
	{
		 int a[]={100,200,300,400,500};
		// enchanted for loop or for...each loop mostly used for arrays concept and collections 
		 // no condition no incrementation 

		for(int x:a) // no index capture new number in a array and store it in a variable and print it
		{
			System.out.println(x);
		}
		
	}

}
